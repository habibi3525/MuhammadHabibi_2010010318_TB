    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
    int selectedRow = tabel3.getSelectedRow();

    if (selectedRow == -1) {
        JOptionPane.showMessageDialog(this, "Pilih baris data yang akan diedit!");
        return;
    }

    isUpdate = true;

    // Ambil data dari tabel pada baris yang dipilih
    String id_barang = tabel3.getValueAt(selectedRow, 0).toString();
    String nama_barang = tabel3.getValueAt(selectedRow, 1).toString();
    String kategori_barang = tabel3.getValueAt(selectedRow, 2).toString();
    String satuan_barang = tabel3.getValueAt(selectedRow, 3).toString();

    // Set data ke komponen input
    textIdbarang.setText(id_barang);
    textNama.setText(nama_barang);
    comboKategori.setSelectedItem(kategori_barang);
    textSatuan.setText(satuan_barang);


    // Berikan keterangan bahwa data yang dipilih akan diupdate
    JOptionPane.showMessageDialog(this, "Anda akan mengedit data dengan ID: " + id_barang);    // TODO add your handling code here:
    }//GEN-LAST:event_btnEditActionPerformed

