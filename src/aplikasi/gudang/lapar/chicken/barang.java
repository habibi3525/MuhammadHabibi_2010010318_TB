    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
    String id_barang = textIdbarang.getText();
    String nama_barang = textNama.getText();
    String kategori_barang = comboKategori.getSelectedItem().toString();
    

    try {
        Connection cn = DriverManager.getConnection("jdbc:mysql://localhost/gudang_lapar", "root", "");

        if (isUpdate) {
            // Update data
            String updateQuery = "UPDATE tbarang SET Nama_barang=?, Kategori_barang=?, Satuan_barang=? WHERE Id_barang=?";
            PreparedStatement pstmt = cn.prepareStatement(updateQuery);
            pstmt.setString(1, nama_barang);
            pstmt.setString(2, kategori_barang);
            pstmt.setString(3, textSatuan.getText());
            pstmt.setString(4, id_barang);
            pstmt.executeUpdate();

            JOptionPane.showMessageDialog(this, "Data berhasil diupdate.");
        } else {
            // Simpan data baru
            String insertQuery = "INSERT INTO tbarang (Id_barang, Nama_barang, Kategori_barang, Satuan_barang) VALUES (?, ?, ?, ?)";
            PreparedStatement pstmt = cn.prepareStatement(insertQuery);
            pstmt.setString(1, id_barang);
            pstmt.setString(2, nama_barang);
            pstmt.setString(3, kategori_barang);
            pstmt.setString(4, textSatuan.getText());
            pstmt.executeUpdate();

            JOptionPane.showMessageDialog(this, "Data berhasil disimpan.");
        }

        // Bersihkan kolom input setelah penyimpanan
        textIdbarang.setText("");
        textNama.setText("");
        comboKategori.setSelectedIndex(0);
        textSatuan.setText("");

        // Segarkan data tabel
        tampilkan();

    } catch (SQLException ex) {
        JOptionPane.showMessageDialog(this, "Error: " + ex.getMessage());
        Logger.getLogger(barang.class.getName()).log(Level.SEVERE, null, ex);
    }
        // TODO add your handling code here:
    }//GEN-LAST:event_btnSimpanActionPerformed

